package src;

public abstract class User implements TarefasMod{
    public String usuario;

    public void User(){
        this.usuario = usuario;
    }

    public void User(String usuario){
        this.usuario = usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public void getUsuario() {
        this.usuario = usuario;
    }
}
